/*WHILE LOOP */

let index = 0;

while(index<=10){
    //console.log(`value of index is : ${index}`);
    index = index + 2;
}

const myArray = ['akash','nikanshu','tushar']
let start = 0;

while(start < myArray.length){
    //console.log(`value is : ${myArray[start]}`)
    start = start + 1;
}

